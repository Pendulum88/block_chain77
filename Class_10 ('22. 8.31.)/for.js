let w = 0;
while (w < 10) {
  console.log(`w = ${w}`);
  ++w;
}

let i = 0;
console.log(`i는 ${i < 5 ? "5보다 작다" : i === 5 ? "5다" : "5보다 크다"}`);
if (i < 5) console.log("i는 5보다 작다");
else if (i === 5) console.log("i 는 5다");
else console.log("i 는 5보다 크다");

for (
  let a = 0, b = 1;
  a < 10 && b > -2;
  ++a, --b // &&는 '그리고'다, 즉 두 조건이 한번에 만족해야한다
) {
  console.log(`a = ${a}, b = ${b}`);
}

for (
  let a = 0, b = 1;
  a < 10 || b > -2;
  ++a, --b // ||는 '또는'이다, 즉 두 조건중 하나라도 만족하면 실행된다
) {
  console.log(`a = ${a}, b = ${b}`);
}

