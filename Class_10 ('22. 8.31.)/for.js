for (let i = 0; i < 10; ++i) {
  // for는 반복문의 명령어 중 하나
  // () 괄호안에 변수 선언, 조건, 반복하며 달라져야할 코드
  // while과 마찬가지로 {}괄호 내의 코드를 실행한다
  // i 선언하고, 조건 확인하고(i<10) 코드실행, ++i실행하고 반복
  // 1 → 2 → {} → 3 → 2 → {} → 3 반복
  console.log(`i = ${i}`);
  //   console.log("i = " + i); 위의 코드와 같다
  // ``(템플릿 리터널)은 텍스트(string)안에 변수를 넣고싶을때 사용한다
  // `i = ${i}` === "i = " + i
  // i는 이 문장 안에서만 존재한다
}

